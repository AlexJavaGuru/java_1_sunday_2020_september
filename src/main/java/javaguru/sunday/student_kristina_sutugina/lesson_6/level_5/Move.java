class Move {

    private int x;
    private int y;

    public Move(int firstCoordinate, int secondCoordinate) {
        this.x = firstCoordinate;
        this.y = secondCoordinate;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
