package javaguru.sunday.student_anna_aleksejeva.lesson_8.task_13;

public class Mango extends Fruit {
    double mangoLength;
    double mangoForm;

    public Mango(String fruitName) {
        super(fruitName);
        this.mangoLength = mangoLength;
        this.mangoForm = mangoForm;
    }

    void fruitfruit() {
        double fruitfruit = mangoLength * mangoForm;
        System.out.println(" Mango fruit is " + fruitfruit);
    }
}
